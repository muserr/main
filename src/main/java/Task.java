public class Task {
    private String name;
    private boolean isDone;

    public Task(String name) {
        this.name = name;
        this.isDone = false;
    }

    public void markAsDone() {
        this.isDone = true;
    }

    public void markAsUndone() {
        this.isDone = false;
    }

    public String toString() {
        String icon = this.isDone ? "\u2713" : "\u2718";
        return "[" + icon + "] " + this.name;
    }
}
